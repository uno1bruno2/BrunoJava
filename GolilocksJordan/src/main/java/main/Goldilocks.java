package main;

public class Goldilocks {
	
	private int maxTemp;
	private int weight;

	public Goldilocks(int maxTemp, int weight) {
		super();
		this.maxTemp = maxTemp;
		this.weight = weight;
	}

	public int getMaxTemp() {
		return maxTemp;
	}

	public void setMaxTemp(int maxTemp) {
		this.maxTemp = maxTemp;
	}

	public int getWeight() {
		return weight;
	}

	public void setWeight(int weight) {
		this.weight = weight;
	}

}
